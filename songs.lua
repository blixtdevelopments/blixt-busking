local function applyDefaultSongLevels(songList, defaultLevel)
    if type(songList) ~= 'table' then return end

    for _, song in ipairs(songList) do
        if song.level == nil then
            song.level = defaultLevel
        end
    end
end

-- Manual song levels:
-- Add `level = 0` to `level = 5` on any song entry to control exactly when it unlocks.
-- Defaults below keep the script working even if a song has no explicit level set yet.
applyDefaultSongLevels(Config.Songs.instrumentals, 0)
applyDefaultSongLevels(Config.Songs.vocals and Config.Songs.vocals.male, 1)
applyDefaultSongLevels(Config.Songs.vocals and Config.Songs.vocals.female, 1)
